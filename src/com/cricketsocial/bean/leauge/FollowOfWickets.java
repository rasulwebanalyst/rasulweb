package com.cricketsocial.bean.leauge;

public class FollowOfWickets {

	
	private String wicketNumber;
	private String runs;
	private String playerName;
	private String overNumber;
	public String getWicketNumber() {
		return wicketNumber;
	}
	public void setWicketNumber(String wicketNumber) {
		this.wicketNumber = wicketNumber;
	}
	public String getRuns() {
		return runs;
	}
	public void setRuns(String runs) {
		this.runs = runs;
	}
	public String getPlayerName() {
		return playerName;
	}
	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}
	public String getOverNumber() {
		return overNumber;
	}
	public void setOverNumber(String overNumber) {
		this.overNumber = overNumber;
	}
	
	
}
