package com.cricketsocial.bean.feed;

public class FeedImage {
	
	private int id;
	private String imageOrVideoByteDatastr;
	private String attachmentType;
	private String attachmentUrl;
	
	public String getImageOrVideoByteDatastr() {
		return imageOrVideoByteDatastr;
	}
	public void setImageOrVideoByteDatastr(String imageOrVideoByteDatastr) {
		this.imageOrVideoByteDatastr = imageOrVideoByteDatastr;
	}
	public String getAttachmentType() {
		return attachmentType;
	}
	public void setAttachmentType(String attachmentType) {
		this.attachmentType = attachmentType;
	}
	public String getAttachmentUrl() {
		return attachmentUrl;
	}
	public void setAttachmentUrl(String attachmentUrl) {
		this.attachmentUrl = attachmentUrl;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
	
	
}
