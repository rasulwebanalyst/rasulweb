package com.cricketsocial.bean.board;

public class CodeVO {
    private int codeId;
    
    private String codeDesc;

    private String refernceColumn;

    private String refernceTable;

	public int getCodeId() {
		return codeId;
	}

	public void setCodeId(int codeId) {
		this.codeId = codeId;
	}

	public String getCodeDesc() {
		return codeDesc;
	}

	public void setCodeDesc(String codeDesc) {
		this.codeDesc = codeDesc;
	}

	public String getRefernceColumn() {
		return refernceColumn;
	}

	public void setRefernceColumn(String refernceColumn) {
		this.refernceColumn = refernceColumn;
	}

	public String getRefernceTable() {
		return refernceTable;
	}

	public void setRefernceTable(String refernceTable) {
		this.refernceTable = refernceTable;
	}

  
}	
