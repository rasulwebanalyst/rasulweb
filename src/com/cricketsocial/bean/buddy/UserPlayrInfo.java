package com.cricketsocial.bean.buddy;

public class UserPlayrInfo {

	private String roleInfo;
	private String battingInfo;
	private String bowlingInfo;
	
	private String userName;
	private String userID;
	private String imgURL;
	
	public String getRoleInfo() {
		return roleInfo;
	}
	public void setRoleInfo(String roleInfo) {
		this.roleInfo = roleInfo;
	}
	public String getBattingInfo() {
		return battingInfo;
	}
	public void setBattingInfo(String battingInfo) {
		this.battingInfo = battingInfo;
	}
	public String getBowlingInfo() {
		return bowlingInfo;
	}
	public void setBowlingInfo(String bowlingInfo) {
		this.bowlingInfo = bowlingInfo;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getImgURL() {
		return imgURL;
	}
	public void setImgURL(String imgURL) {
		this.imgURL = imgURL;
	}
	
	
	
}
