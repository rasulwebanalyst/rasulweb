package com.cricketsocial.bean.buddy;

public class ImageDetails {

	
	private String imageName;
	private byte[] imageInbyteArray;
	private String mimeType;
	public String getImageName() {
		return imageName;
	}
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	public byte[] getImageInbyteArray() {
		return imageInbyteArray;
	}
	public void setImageInbyteArray(byte[] imageInbyteArray) {
		this.imageInbyteArray = imageInbyteArray;
	}
	public String getMimeType() {
		return mimeType;
	}
	public void setMimeType(String mimeType) {
		this.mimeType = mimeType;
	}
	
	
	
	
	
}
