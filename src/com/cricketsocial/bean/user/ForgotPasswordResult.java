package com.cricketsocial.bean.user;

public class ForgotPasswordResult {
	private String forgotPasswordResultType;
	private String  forgotPasswordResult;
	private String  forgotPasswordResultStatus;
	public String getForgotPasswordResultType() {
		return forgotPasswordResultType;
	}
	public void setForgotPasswordResultType(String forgotPasswordResultType) {
		this.forgotPasswordResultType = forgotPasswordResultType;
	}
	public String getForgotPasswordResult() {
		return forgotPasswordResult;
	}
	public void setForgotPasswordResult(String forgotPasswordResult) {
		this.forgotPasswordResult = forgotPasswordResult;
	}
	public String getForgotPasswordResultStatus() {
		return forgotPasswordResultStatus;
	}
	public void setForgotPasswordResultStatus(String forgotPasswordResultStatus) {
		this.forgotPasswordResultStatus = forgotPasswordResultStatus;
	}
	
	
}
