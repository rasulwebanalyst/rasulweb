package com.cricketsocial.bean.roaster;

public class FollowOfWickets {
	
	private int wicketNumber;
	private int runs;
	private String playerName;
	private double overNumber;
	
	
	public int getWicketNumber() {
		return wicketNumber;
	}
	public void setWicketNumber(int wicketNumber) {
		this.wicketNumber = wicketNumber;
	}
	public int getRuns() {
		return runs;
	}
	public void setRuns(int runs) {
		this.runs = runs;
	}
	public String getPlayerName() {
		return playerName;
	}
	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}
	public double getOverNumber() {
		return overNumber;
	}
	public void setOverNumber(double overNumber) {
		this.overNumber = overNumber;
	}
	
	
	

}
